package while2;
import java.util.Scanner;
/*Faça um programa que leia um nome de usuário e a sua senha e não aceita a senha igual ao 
nome do usuário, mostrando uma mensagem de erro e voltando a pedir as informações */
public class exercicio2_NomeIgualSenha {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		boolean valido = false;
		String nome;
		String senha;
		
		do {
			System.out.println("Entre com um nome");
			nome = scan.next();
			System.out.println("Entre com uma senha");
			senha = scan.next();
			if(nome.equals(senha)) {
				valido = true;
				System.out.println("Nome e senha válidos");
			}else {
				System.out.println("Não aceitamos senha igual a do usuário");
			}
			
		}while(!valido);
		System.out.println("Fim do programa");
	}

}
